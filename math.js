let a = 5.5
//無條件捨去
console.log(Math.floor(a))
//無條件進位
console.log(Math.ceil(a))
//四捨五入
console.log(Math.round(a))

let b = 2
//計算次方數答案，後面數字代表次方數字
console.log(Math.pow(b,10))

let c = -8
//絕對值
console.log(Math.abs(c))

let d = 25
//開根號
console.log(Math.sqrt(d))